#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new;
	int		i;

	new = malloc((ft_strlen(src) + 1) * sizeof(char));
	if (!(new))
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
